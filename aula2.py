'''
sep=' '
Com esse comando é utilizado o separador que é posto entre as aspas
end='\r\n'
End é informação no final do código.
\r\n é um comando para quebra de linha
\r\n para windows
\n para outros sistemas operacionais
Testar o código abaixo para entender
'''
print(12, 34, sep='-', end='**')
print(45, 56, sep="-",end='*\n')
print(78, 91, sep="-",end='\r\n**')
